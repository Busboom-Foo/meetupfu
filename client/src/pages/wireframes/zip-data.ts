/** Haversine distance in miles between two lat/lon points. */
export function haversineDistance(
  lat1: number, lon1: number,
  lat2: number, lon2: number,
): number {
  const R = 3958.8; // Earth radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon / 2) * Math.sin(dLon / 2);
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/** Look up a zip code entry. Returns undefined if not found. */
export function findZip(zip: string): ZipEntry | undefined {
  return zipData.find((z) => z.zip === zip);
}

/** Find a zip entry by neighborhood name (case-insensitive, partial match). */
export function findByName(query: string): ZipEntry | undefined {
  const q = query.toLowerCase().trim();
  // Exact match first
  const exact = zipData.find((z) => z.neighborhood.toLowerCase() === q);
  if (exact) return exact;
  // Partial match
  return zipData.find((z) => z.neighborhood.toLowerCase().includes(q));
}

/** Resolve a user query (zip or city name) to coordinates. */
export function resolveLocation(query: string): { lat: number; lon: number; label: string } | null {
  const trimmed = query.trim();
  // Try as zip code first
  if (/^\d{5}$/.test(trimmed)) {
    const entry = findZip(trimmed);
    if (entry) return { lat: entry.lat, lon: entry.lon, label: entry.neighborhood };
  }
  // Try as city/neighborhood name
  const entry = findByName(trimmed);
  if (entry) return { lat: entry.lat, lon: entry.lon, label: entry.neighborhood };
  return null;
}
